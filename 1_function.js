function add(num1, num2){
    return num1 + num2
}
console.log(add(4,5))


// function expression 
const subtraction = function(num1, num2){
    return num1 + num2
}

console.log(subtraction(7,5))

// arrow function 
const multiplication = (num1, num2) => {
    return num1 * num2
}
console.log(multiplication(4,5))